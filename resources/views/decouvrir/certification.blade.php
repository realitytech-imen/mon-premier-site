                        <h4 class="title">LA FORMATION</h4>
                        <div class="content">
                            C’est l’étape primordiale du parcours. Pendant la formation, l’étudiant suit et s’implique dans la construction de son savoir à travers des mises en situations pratiques. Des cours essentiellement pratiques et quelques fois théoriques enseignés par nos experts formateurs. Chaque chapitre ou module de formation bouclé est soldé par la restitution des savoirs, compétences et aptitudes acquis à travers un exercice pratique auquel l’étudiant est soumis. Les notes obtenues au cours de ces évaluations sont comptabilisées et leur somme permet à l’étudiant de valider ou non son parcours de formation.
                        </div>

                        <h4 class="title">LE MOIS DE LA PRATIQUE</h4>
                        <div class="content">
                            La réalisation d’un projet est une étape décisive et précède la soutenance. Durant un mois entier, l’étudiant réalise un projet personnel sous la supervision et le guide d’un mentor ou professionnel de son domaine qui lui est spécialement affecté pour l’occasion. 
                        </div>

                        <h4 class="title">LA SOUTENANCE</h4>
                        <div class="content">
                            C’est la dernière étape du processus. Elle consiste pour l’étudiant à présenter son projet personnel réalisé devant un jury composé d’experts et de professionnels de son domaine qui apprécieront travail à travers des critères bien définis. La décision de ce jury conditionne la validation ou non du projet de l’étudiant et son admission pour l’obtention du parchemin qu’est le certificat. Une fois l’étape de la certification passée avec succès, l’étudiant obtient son certificat au cours d’une cérémonie officielle.
                        </div>

                        <h4 class="title">LA CERTIFICATION</h4>
                        <div class="content">
                            La certification représente l'aboutissement ultime pour les étudiants. Elle récompense leurs efforts, leur implication, et leur persévérance tout au long de leur parcours, couronné par une brillante soutenance de projet. L’école organise, pour marquer cet accomplissement, une cérémonie de remise de certificats, où les diplômés reçoivent le parchemin symbolisant leur réussite.
                        </div>

                        <h4 class="title">LE STAGE EN ENTREPRISE</h4>
                        <div class="content">
                        Le stage en entreprise constitue une étape clé, aussi essentielle que les autres phases de la certification. Une fois certifiés, les étudiants bénéficient de l’appui de l’IMeN, notamment lors de "<strong>La Foire de l’Emploi</strong>", où ils rencontrent les entreprises partenaires pour des entretiens visant leur insertion professionnelle. Le stage offre une opportunité précieuse : prouver leurs compétences, se démarquer et potentiellement obtenir un contrat de travail. Grâce au Programme d’Insertion Professionnelle (PIP) de l’institut, les étudiants sont préparés pour répondre aux attentes du marché et réussir cette transition vers le monde professionnel.
